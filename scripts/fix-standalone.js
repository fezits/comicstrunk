// Fixes Next.js standalone output on Windows by replacing broken symlinks with real copies
const fs = require('fs');
const path = require('path');

const standalone = path.join(__dirname, '..', 'apps', 'web', '.next', 'standalone');
const pnpmStore = path.join(__dirname, '..', 'node_modules', '.pnpm');

// Map of package names to their locations in .pnpm store
function findPkg(name) {
  const entries = fs.readdirSync(pnpmStore);
  for (const entry of entries) {
    if (entry.startsWith(name.replace('/', '+') + '@')) {
      const pkgPath = path.join(pnpmStore, entry, 'node_modules', ...name.split('/'));
      if (fs.existsSync(pkgPath)) return pkgPath;
    }
  }
  return null;
}

function copyRecursive(src, dest) {
  if (!fs.existsSync(src)) return;
  fs.mkdirSync(dest, { recursive: true });
  for (const entry of fs.readdirSync(src, { withFileTypes: true })) {
    const srcPath = path.join(src, entry.name);
    const destPath = path.join(dest, entry.name);
    if (entry.isDirectory()) {
      copyRecursive(srcPath, destPath);
    } else {
      fs.copyFileSync(srcPath, destPath);
    }
  }
}

// Fix root node_modules
const rootNM = path.join(standalone, 'node_modules');
const packages = ['next', 'react', 'react-dom', '@next/env', '@swc/helpers', 'client-only', 'caniuse-lite', 'nanoid', 'detect-libc', 'styled-jsx', '@img/sharp-linux-x64'];

for (const pkg of packages) {
  const dest = path.join(rootNM, ...pkg.split('/'));
  if (fs.existsSync(dest)) continue;

  const src = findPkg(pkg);
  if (src) {
    console.log(`Copying ${pkg}...`);
    copyRecursive(src, dest);
  }
}

// Fix apps/web/node_modules
const appNM = path.join(standalone, 'apps', 'web', 'node_modules');
for (const pkg of ['react', 'react-dom', 'next']) {
  const dest = path.join(appNM, ...pkg.split('/'));
  if (fs.existsSync(dest)) continue;

  const src = findPkg(pkg);
  if (src) {
    console.log(`Copying ${pkg} to apps/web/...`);
    copyRecursive(src, dest);
  }
}

// Copy server.js from next package to standalone/apps/web/ if missing
const serverJs = path.join(standalone, 'apps', 'web', 'server.js');
if (!fs.existsSync(serverJs)) {
  const nextPkg = findPkg('next');
  if (nextPkg) {
    const nextServerJs = path.join(nextPkg, 'dist', 'server', 'lib', 'render-server-standalone.js');
    // Actually, server.js is generated by Next.js build, not from the package
    // We need to check if it exists in the next required-server-files
    const requiredServerFiles = path.join(standalone, 'apps', 'web', '.next', 'required-server-files.json');
    if (fs.existsSync(requiredServerFiles)) {
      console.log('required-server-files.json exists, server.js should be generated');
    }

    // Copy the minimal server from next
    const minimalServer = path.join(nextPkg, 'dist', 'server', 'next-server.js');
    if (fs.existsSync(minimalServer)) {
      // Generate a minimal server.js
      const content = `
process.env.__NEXT_PRIVATE_STANDALONE_CONFIG = require("path").join(__dirname, ".next", "required-server-files.json");
require("next/dist/server/next-server");
const NextServer = require("next/dist/server/next-server").default;
const path = require("path");

const nextServer = new NextServer({
  hostname: process.env.HOSTNAME || "0.0.0.0",
  port: parseInt(process.env.PORT || "3000", 10),
  dir: path.join(__dirname),
  dev: false,
  customServer: false,
  conf: JSON.parse(require("fs").readFileSync(path.join(__dirname, ".next", "required-server-files.json"), "utf8")).config,
});

const handler = nextServer.getRequestHandler();

require("http")
  .createServer(async (req, res) => {
    try {
      await handler(req, res);
    } catch (err) {
      console.error(err);
      res.statusCode = 500;
      res.end("Internal Server Error");
    }
  })
  .listen(parseInt(process.env.PORT || "3000", 10), process.env.HOSTNAME || "0.0.0.0", () => {
    console.log(\`> Ready on http://\${process.env.HOSTNAME || "0.0.0.0"}:\${process.env.PORT || "3000"}\`);
  });
`;
      fs.writeFileSync(serverJs, content.trim());
      console.log('Generated server.js');
    }
  }
}

console.log('Done!');
